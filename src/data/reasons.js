/**
 * 根據卡牌 ID 和語氣獲取請假理由
 * @param {number} cardId - 卡牌 ID (1-5)
 * @param {string} tone - 語氣 ('平靜' | '中二' | '社恐')
 * @returns {string} 請假理由文本
 */
export const getReason = (cardId, tone) => {
  const key = `${cardId}-${tone}`
  return reasons[key] || '占卜失敗，請重試'
}
